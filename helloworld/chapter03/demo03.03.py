print("请输入玫瑰花🌹数")
# 获取用户输入的玫瑰花数,并转换成整形
number = int(input("输入您像送几朵玫瑰花,小莫会告诉你含义:"))
if number == 1:
    print("1朵,代表唯一")
elif number == 3:
    print("3朵,i love you")
elif number == 10:
    print("10朵,十全十美")
elif number == 99:
    print("99朵,天长地久")
elif number == 108:
    print("108朵,求婚")
else:
    print("不知道")
